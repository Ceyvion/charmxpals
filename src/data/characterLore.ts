type StatKey = 'rhythm' | 'style' | 'power' | 'flow' | 'teamwork';

export const worldTagline = 'When the music calls, the worlds collide.';

function artRefsFor(slug: string): Record<string, string> {
  const base = `/assets/characters/${slug}`;
  return {
    signature: `${base}/signature.webp`,
    thumbnail: `${base}/thumb.webp`,
    card: `${base}/card.webp`,
    portrait: `${base}/portrait.webp`,
    banner: `${base}/banner.webp`,
    full: `${base}/portrait.webp`,
    sprite: `${base}/sprite.webp`,
  };
}

function buildStats(focus: StatKey, idx: number): Record<StatKey, number> {
  const stats: Record<StatKey, number> = {
    rhythm: 74 + ((idx * 3) % 16),
    style: 73 + ((idx * 5) % 16),
    power: 72 + ((idx * 7) % 16),
    flow: 75 + ((idx * 4) % 16),
    teamwork: 73 + ((idx * 6) % 16),
  };
  stats[focus] = Math.min(99, stats[focus] + 14);
  const keys: StatKey[] = ['rhythm', 'style', 'power', 'flow', 'teamwork'];
  const secondary = keys[(keys.indexOf(focus) + 2) % keys.length];
  stats[secondary] = Math.min(99, stats[secondary] + 6);
  return stats;
}

